//************Program for factorial**********************
#include<iostream>
using namespace std;

int main()
{
    int n, num=1;
    cout<<"Tell me the number whose factorial do you want:: "<<endl;
    cin>>n;

    for (int i = 1; i <= n; i++)
    {
        num= num*i;
        
    }
    cout<<"The factorial of number is:: "<<num;
    return 0; 
}
